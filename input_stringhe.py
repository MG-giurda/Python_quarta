parola = input("inserici una parola -> ")
# 0 è il l'indice del primo carattere della stringa perchè la stringa è un vettore di caratteri 
# quindi se prende 0 prende il carattere nella cella 0 
# per prendere il caratere si possono mettere anche i numeri negativi ma l'ultimo carattere è -1 e il penultimo sarà -2 e così via 
print(f"il primo carattere è {parola[0]}")
